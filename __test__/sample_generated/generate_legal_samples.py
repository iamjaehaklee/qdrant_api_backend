def generate_samples_for_case(case_name: str, config: dict) -> int:
    """특정 사건 유형에 대한 샘플 생성"""

    base_dir = Path(__file__).parent
    output_dir = base_dir / case_name / "ocr_summaries"
    output_dir.mkdir(parents=True, exist_ok=True)

    documents = config["documents"]
    project_id = config["project_id"]
    case_info = config["case_info"]

    # 70개가 안되면 일반 증빙자료로 채움
    while len(documents) < 70:
        idx = len(documents) + 1
        documents.append((f"증빙자료_{idx:02d}호", f"사건 관련 증빙 자료 제{idx}호"))

    # 70개로 제한
    documents = documents[:70]

    base_date = datetime(2024, 1, 1, 9, 0, 0)

    for i, (filename, description) in enumerate(documents, start=1):
        created_at = base_date + timedelta(days=i-1, hours=i % 24)

        # Summary text 생성 (사건 정보 포함)
        summary_parts = [description]
        for key, value in case_info.items():
            summary_parts.append(f"{key}: {value}")
        summary_text = f"{description}. " + ", ".join(f"{k}={v}" for k, v in case_info.items())

        summary_data = {
            "summary_id": str(uuid.uuid4()),
            "project_id": project_id,
            "file_id": i,
            "summary_text": summary_text[:500],  # Limit length
            "created_at": created_at.isoformat() + "+00:00"
        }

        output_file = output_dir / f"{filename}.json"
        with open(output_file, 'w', encoding='utf-8') as f:
            json.dump(summary_data, f, ensure_ascii=False, indent=2)

    return len(documents)


def main():
    """모든 사건 유형 샘플 생성"""

    print("="*70)
    print("법률사무소 사건별 OCR Summary 샘플 생성 시작")
    print("="*70)

    total_generated = 0

    for case_name, config in CASE_TEMPLATES.items():
        print(f"\n📁 {case_name} 생성 중...")
        count = generate_samples_for_case(case_name, config)
        total_generated += count
        print(f"✅ {case_name}: {count}개 파일 생성 완료")

    print("\n" + "="*70)
    print(f"🎉 전체 생성 완료: 총 {total_generated}개 파일")
    print(f"📊 사건 유형: {len(CASE_TEMPLATES)}개")
    print(f"📈 평균: {total_generated // len(CASE_TEMPLATES)}개/사건")
    print("="*70)


if __name__ == "__main__":
    main()
